package day07_Nov17Sun;

public class RelationalOperators {

    public static void main(String[] args) {

        //these are boolean statements
        //used for comparing 2 numerical values

        System.out.println(7>5);
        System.out.println(7<-19);
        System.out.println(5>= 5);
        System.out.println(7>=8);
        System.out.println(4<=5);
        System.out.println(17 >= 8);

        System.out.println(5==5);  //this is used for checking equality

        System.out.println(6 != 10);  //this means checking for inequality; NOT equal




    }
}
