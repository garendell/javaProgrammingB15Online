package day07_Nov17Sun;

public class RelationalOperators_Variables {
    public static void main(String[] args) {

        int myNumber=10;
        System.out.println(myNumber>5);
        System.out.println(myNumber<-19);
        System.out.println(myNumber>= 5);
        System.out.println(myNumber>=8);
        System.out.println(myNumber<=5);
        System.out.println(myNumber >= 8);

        System.out.println(myNumber==5);  //this is used for checking equality

        System.out.println(myNumber != 10);  //this means checking for inequality; NOT equal

        System.out.println(myNumber==5);
    }
}
